class TestARP:
    def test_arp_reports_ethertype_only(self):
        d = decode_packet_headers(_hex(_ARP_HEX))
        assert d["eth.ethertype"] == 0x0806
        # No IP fields, no L4 fields.
        assert "ip.src" not in d
        assert "tcp.src_port" not in d
        assert "udp.src_port" not in d
        # Not a truncation — the layer simply doesn't apply.
        assert "_truncated_at" not in d


class TestTruncation:
    def test_truncated_ip(self):
        d = decode_packet_headers(_hex(_TRUNCATED_IP_HEX))
        # Ethernet still decoded.
        assert d["eth.ethertype"] == 0x0800
        # IP layer truncated.
        assert d.get("_truncated_at") == "ip"
        # No L4 fields.
        assert "tcp.src_port" not in d

    def test_truncated_tcp_payload(self):
        # Take just the eth + IPv4 portion of the SYN packet (54 bytes total,
        # 34 bytes is eth+IPv4 and 10 bytes of TCP).
        truncated_hex = _IPV4_TCP_SYN_HEX[: 2 * (14 + 20 + 10)]
        d = decode_packet_headers(_hex(truncated_hex))
        # IP fields populated, TCP partially decoded then truncated.
        assert d["ip.version"] == 4
        assert d.get("_truncated_at") == "tcp"


def test_ipv4_tcp_hex_length_sanity():
    """Sanity-check fixture sizes match expected Ethernet + IPv4 + TCP."""
    assert len(_hex(_IPV4_TCP_SYN_HEX)) == 14 + 20 + 20
    # UDP fixture is eth + IPv4 + UDP header + 20-byte zero payload.
    assert len(_hex(_IPV4_UDP_HEX)) == 14 + 20 + 8 + 20
