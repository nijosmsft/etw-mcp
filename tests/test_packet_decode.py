# Only the first 12 bytes of an Ethernet header — truncated mid-header.
_TRUNCATED_ETH_HEX = "aabbccddeeff112233445566"


# Ethernet + IPv4 but cut off at the IP header (only first 8 bytes of IP).
_TRUNCATED_IP_HEX = (
    "aabbccddeeff112233445566"
    "0800"
    "4500002800000000"  # 8 of 20 IPv4 header bytes
)


def _hex(s: str) -> bytes:
    return bytes.fromhex(s)


class TestEthernet:
    def test_macs_and_ethertype(self):
        d = decode_packet_headers(_hex(_IPV4_TCP_SYN_HEX))
        assert d["eth.dst"] == "aa:bb:cc:dd:ee:ff"
        assert d["eth.src"] == "11:22:33:44:55:66"
        assert d["eth.ethertype"] == 0x0800

    def test_truncated_eth(self):
        d = decode_packet_headers(_hex(_TRUNCATED_ETH_HEX))
        assert d.get("_truncated_at") == "eth"
        # No fields populated because the header didn't fully arrive.
        assert "eth.src" not in d

    def test_empty_bytes(self):
        d = decode_packet_headers(b"")
        assert d.get("_truncated_at") == "eth"


class TestIPv4TCP:
    def test_ipv4_fields(self):
        d = decode_packet_headers(_hex(_IPV4_TCP_SYN_HEX))
        assert d["ip.version"] == 4
        assert d["ip.proto"] == 6
        assert d["ip.src"] == "10.0.0.1"
        assert d["ip.dst"] == "10.0.0.2"
        assert d["ip.ttl"] == 64
        assert d["ip.total_length"] == 40
        assert d["ip.header_len"] == 20

    def test_tcp_fields(self):
        d = decode_packet_headers(_hex(_IPV4_TCP_SYN_HEX))
        assert d["tcp.src_port"] == 1234
        assert d["tcp.dst_port"] == 80
        assert d["tcp.seq"] == 1000
        assert d["tcp.ack"] == 0
        assert d["tcp.data_offset"] == 5
        assert d["tcp.window"] == 8192
        assert d["tcp.flags"] == ["SYN"]
        assert d["tcp.flags_raw"] == 0x02


